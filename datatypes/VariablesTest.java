package datatypes;

public class VariablesTest {

	public static void main(String[] args) {

		int num;
		float f1;
		boolean bool1;
		char ch;
		double pi = 3.14159;
		num=20;
		f1=20.34f;
		bool1=true;
		ch = 'A';
		System.out.println("The num value is " +num);
		System.out.println("The f1 value is "+f1);
		System.out.println("The bool1 value is "+bool1);
		System.out.println("The ch value is "+ch);
		System.out.println("The pi value is "+pi);
		num++;
		bool1=false;
		ch = 'a';
		System.out.println("The changed num value is "+num);
		System.out.println("The changed bool1 value is "+bool1);
		System.out.println("The changed ch value is "+ch);
		short s1=2772;
		short s2=-2323;
		System.out.println(s1);
		System.out.println(s2);
		
	}
}