package datatypes;

public class VariablePlay3 {

	public static void main(String[] args) {
		int a1 = 20;
		System.out.println(a1);
		float f1 = (float)a1;
		//float f1 = a1;
		System.out.println(f1);
		
		float f2 = 30.33f;
		System.out.println(f2);
		int a2 = (int)f2;
		System.out.println(a2);
		
		char ch='A';
		System.out.println(ch);
	}
}