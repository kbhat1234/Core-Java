package loops;

public class ForLoopTest {

	public static void main(String[] args) {
		for(int i=1; i<=5; i++) {
			System.out.println(i);
		}
		System.out.println();
		for(int j=5; j>=1; j--) {
			System.out.println(j);
		}
		System.out.println();
		for(int k=0; k<=10; k+=2) {
			System.out.println(k);
		}
		System.out.println();
	}
}